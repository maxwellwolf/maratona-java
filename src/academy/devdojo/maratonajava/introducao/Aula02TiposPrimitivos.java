package academy.devdojo.maratonajava.introducao;

public class Aula02TiposPrimitivos {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, long, boolean
        int age = 36;
        double salarioDouble = 5400;
        float salariofloat = 5400;
        byte idadeByte = 36;
        short idadeShort = 36;
        boolean verdadeiro = true;
        boolean falso =  false;
        char caractere = 'M';
        String nome = "Maxwell Machado";
        System.out.println("Idade Int: " + age);
        System.out.println("Salario Double: " + salarioDouble);
        System.out.println("Salario Float: " + salariofloat);
        System.out.println("Idade byte: " + idadeByte);
        System.out.println("Idade short: " + idadeShort);
        System.out.println("Verdadeiro: " + verdadeiro);
        System.out.println("Falso: " + falso);
        System.out.println("Caractere: " + caractere);
        System.out.println("Nome: " + nome);
    }
}
